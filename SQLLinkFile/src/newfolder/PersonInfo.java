package newfolder;

public class PersonInfo {

	private int PersonID;
	private String FirstName;
	private String LastName; 
	private String TricareRegion;
	
	public int getPersonID() {return this.PersonID;}
    public void setPersonID(int pID) {this.PersonID = pID;}
    
    public String getFirstName() {return this.FirstName;}
    public void setFirstName(String fName) {this.FirstName = fName;}
    
    public String getLastName() {return this.LastName;}
    public void setLastName(String lName) {this.LastName = lName;}
    
    public String getTricareRegion() {return this.TricareRegion;}
    public void setTricareRegion(String tRegion) {this.TricareRegion = tRegion;}
    
}
